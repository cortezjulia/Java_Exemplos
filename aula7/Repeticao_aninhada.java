
public class Repeticao_aninhada {
    public static void main(String[] args) {
        for (int i = 0; i < 11; i++) {
            for (int j = 0; j < 11; j++) {
                System.out.println(i + " x " + j + " = " + (i * j));
            }
            System.out.println('\n');
        }

    }

}